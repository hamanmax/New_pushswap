#include "../include/libft.h"

void	ft_bzero(void *b, size_t len)
{
	size_t	i;
	char	*s;

	s = (char *)b;
	i = 0;
	while (len > 0)
	{
		s[i] = '\0';
		i++;
		len--;
	}
}
