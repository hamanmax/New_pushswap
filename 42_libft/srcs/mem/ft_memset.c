#include "../include/libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*s;

	s = (char *)b;
	i = 0;
	while (len > 0)
	{
		s[i] = c;
		i++;
		len--;
	}
	return (b);
}
